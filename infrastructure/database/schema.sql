-- 6. LLM Usage Logs Table
CREATE TABLE IF NOT EXISTS public.llm_usage_logs (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    prompt_tokens integer NOT NULL DEFAULT 0,
    completion_tokens integer NOT NULL DEFAULT 0,
    cost_usd numeric NOT NULL DEFAULT 0.0,
    timestamp timestamptz DEFAULT now()
);

-- Index for performance tracking
CREATE INDEX IF NOT EXISTS idx_llm_usage_logs_user_time ON public.llm_usage_logs(user_id, timestamp DESC);

-- Enable RLS
ALTER TABLE public.llm_usage_logs ENABLE ROW LEVEL SECURITY;

-- RLS Policies
