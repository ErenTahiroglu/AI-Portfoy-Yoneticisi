-- 1. Portfolios Table
CREATE TABLE IF NOT EXISTS public.portfolios (
    user_id uuid PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    tickers jsonb NOT NULL DEFAULT '[]'::jsonb,
    updated_at timestamptz DEFAULT now()
);

-- Enable RLS
ALTER TABLE public.portfolios ENABLE ROW LEVEL SECURITY;

-- RLS Policies
CREATE POLICY "Users can view their own portfolio" 
    ON public.portfolios FOR SELECT 
    USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own portfolio" 
    ON public.portfolios FOR INSERT 
    WITH CHECK (auth.uid() = user_id);

CREATE POLICY "Users can update their own portfolio" 
    ON public.portfolios FOR UPDATE 
    USING (auth.uid() = user_id);

CREATE POLICY "Users can delete their own portfolio" 
    ON public.portfolios FOR DELETE 
    USING (auth.uid() = user_id);


-- 2. Paper Trades Table
CREATE TABLE IF NOT EXISTS public.paper_trades (
    id bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id uuid NOT NULL REFERENCES auth.users(id) ON DELETE CASCADE,
    symbol text NOT NULL,
    order_type text NOT NULL CHECK (order_type IN ('BUY', 'SELL')),
    target_weight numeric NOT NULL,
    execution_price numeric DEFAULT 0.0,
    timestamp timestamptz DEFAULT now()
);

-- Index for performance
CREATE INDEX IF NOT EXISTS idx_paper_trades_user_timestamp ON public.paper_trades(user_id, timestamp DESC);

-- Enable RLS
ALTER TABLE public.paper_trades ENABLE ROW LEVEL SECURITY;

-- RLS Policies
CREATE POLICY "Users can view their own trades" 
    ON public.paper_trades FOR SELECT 
    USING (auth.uid() = user_id);

CREATE POLICY "Users can insert their own trades" 
    ON public.paper_trades FOR INSERT 
    WITH CHECK (auth.uid() = user_id);


